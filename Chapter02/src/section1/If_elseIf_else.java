package section1;

public class If_elseIf_else {
    public static void main(String[] args) {
        int score = 84;

        if(score >= 90){
            System.out.println("A");
        }else if(score >= 80){
            System.out.println("B");
        }else if(score >= 70){
            System.out.println("C");
        }else{
            System.out.println("D");
        }

    }
}
